    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        
        String nombreCliente = txtNombreCliente.getText();
        String marca = cbxMarca.getSelectedItem().toString();
        String modelo = txtModelo.getText();
        String color = cbxColor.getSelectedItem().toString();
        int numPuertas = Integer.parseInt(txtNumPuertas.getText());
        String motor = txtMotor.getText();
        int kilometraje = Integer.parseInt(txtKilometraje.getText());
        
        oVenta = new Venta(nombreCliente, marca, modelo, color, numPuertas, motor, kilometraje);
        
        oVenta.calcularPrecioFinal();
        String respuesta = oVenta.obtenerInformacion();
        txtRespuesta.setText(respuesta);
    }//GEN-LAST:event_btnCalcularActionPerformed

