  OBJETIVO:
    Implementar un "avisador" (journal) de cambios en OLTP para ETL incremental:

      - Detectar INSERT / UPDATE / DELETE en tablas OLTP
      - Registrar qué cambió (BusinessKey) y cuándo (ChangeDtm)
      - Permitir al DW consumir incrementalmente usando watermark (AuditId)

  ¿DÓNDE SE INSTALA?
    ✅ OLTP: BDVENTAS
    ❌ No en DW (DW solo consulta BDVENTAS.aud.AUD_CAMBIOS)

  TABLAS AUDITADAS:
    - dbo.CLIENTE
    - dbo.PRODUCTO
    - dbo.VENDEDOR
    - dbo.DISTRITO
