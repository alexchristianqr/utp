  NOTA SOBRE SQL SERVER:
    - Trigger sobre tabla dbo.X debe estar en dbo (mismo esquema).

  NOTA DE CALIDAD:
    - Los triggers de UPDATE aquí solo registran filas donde realmente cambió algo.
    - Esto evita ruido (updates que no modifican valores).

  Alternativas en producción:
    - CDC (Change Data Capture)
