CREATE TRIGGER dbo.trg_CLIENTE_AUD
ON dbo.CLIENTE
AFTER INSERT, UPDATE, DELETE
AS
BEGIN
    SET NOCOUNT ON;

    -- INSERT
    INSERT INTO aud.AUD_CAMBIOS(Tabla,Operacion,BusinessKey,BeforeJson,AfterJson)
    SELECT 'CLIENTE','I', CONCAT('COD_CLI=', i.COD_CLI),
           NULL, (SELECT i.* FOR JSON PATH, WITHOUT_ARRAY_WRAPPER)
    FROM inserted i
    LEFT JOIN deleted d ON d.COD_CLI=i.COD_CLI
    WHERE d.COD_CLI IS NULL;

    -- DELETE
    INSERT INTO aud.AUD_CAMBIOS(Tabla,Operacion,BusinessKey,BeforeJson,AfterJson)
    SELECT 'CLIENTE','D', CONCAT('COD_CLI=', d.COD_CLI),
           (SELECT d.* FOR JSON PATH, WITHOUT_ARRAY_WRAPPER), NULL
    FROM deleted d
    LEFT JOIN inserted i ON i.COD_CLI=d.COD_CLI
    WHERE i.COD_CLI IS NULL;

    -- UPDATE (solo si hay cambios reales)
    INSERT INTO aud.AUD_CAMBIOS(Tabla,Operacion,BusinessKey,BeforeJson,AfterJson)
    SELECT 'CLIENTE','U', CONCAT('COD_CLI=', i.COD_CLI),
           (SELECT d.* FOR JSON PATH, WITHOUT_ARRAY_WRAPPER),
           (SELECT i.* FOR JSON PATH, WITHOUT_ARRAY_WRAPPER)
    FROM inserted i
    JOIN deleted d ON d.COD_CLI=i.COD_CLI
    WHERE
        ISNULL(i.RSO_CLI,'') <> ISNULL(d.RSO_CLI,'')
     OR ISNULL(i.DIR_CLI,'') <> ISNULL(d.DIR_CLI,'')
     OR ISNULL(i.TLF_CLI,'') <> ISNULL(d.TLF_CLI,'')
