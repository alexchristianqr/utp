IF OBJECT_ID('aud.AUD_CAMBIOS','U') IS NULL
BEGIN
    CREATE TABLE aud.AUD_CAMBIOS(
        AuditId        BIGINT IDENTITY(1,1) NOT NULL PRIMARY KEY,

        Tabla          SYSNAME        NOT NULL,  -- CLIENTE/PRODUCTO/VENDEDOR/DISTRITO/FACTURA/DETALLE_FACTURA
        Operacion      CHAR(1)        NOT NULL,  -- I/U/D
        BusinessKey    VARCHAR(200)   NOT NULL,  -- clave como texto genérico

        ChangeDtm      DATETIME2(3)   NOT NULL CONSTRAINT DF_AUD_CAMBIOS_ChangeDtm DEFAULT SYSDATETIME(),

        LoginName      SYSNAME        NULL CONSTRAINT DF_AUD_CAMBIOS_Login DEFAULT ORIGINAL_LOGIN(),
        UserName       SYSNAME        NULL CONSTRAINT DF_AUD_CAMBIOS_User  DEFAULT SUSER_SNAME(),
        HostName       SYSNAME        NULL CONSTRAINT DF_AUD_CAMBIOS_Host  DEFAULT HOST_NAME(),
        AppName        SYSNAME        NULL CONSTRAINT DF_AUD_CAMBIOS_App   DEFAULT APP_NAME(),

        BeforeJson     NVARCHAR(MAX)  NULL,
        AfterJson      NVARCHAR(MAX)  NULL
    );

    CREATE INDEX IX_AUD_CAMBIOS_ChangeDtm ON aud.AUD_CAMBIOS(ChangeDtm, AuditId);
    CREATE INDEX IX_AUD_CAMBIOS_TablaDtm  ON aud.AUD_CAMBIOS(Tabla, ChangeDtm, AuditId);
END
