/*============================================================
  5) VALIDACIÓN FORMAL: ¿OLTP realmente permite I/U/D en FACTURA/DETALLE?
  ------------------------------------------------------------
  Procedimiento NO destructivo: usa TRAN + ROLLBACK.
============================================================*/
CREATE OR ALTER PROCEDURE aud.usp_Validate_DML_Factura_Detalle
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE @NumFac VARCHAR(12) = CONCAT('TST', RIGHT(CONVERT(VARCHAR(20),ABS(CHECKSUM(NEWID()))), 9));
    DECLARE @CodPro CHAR(5);
    DECLARE @CodCli CHAR(5);
    DECLARE @CodVen CHAR(3);

    SELECT TOP 1 @CodPro = COD_PRO FROM dbo.PRODUCTO ORDER BY COD_PRO;
    SELECT TOP 1 @CodCli = COD_CLI FROM dbo.CLIENTE  ORDER BY COD_CLI;
    SELECT TOP 1 @CodVen = COD_VEN FROM dbo.VENDEDOR ORDER BY COD_VEN;

    IF @CodPro IS NULL OR @CodCli IS NULL OR @CodVen IS NULL
        THROW 51001, 'OLTP VALIDATION: faltan maestros para probar FACTURA.', 1;

    DECLARE @Antes BIGINT = ISNULL((SELECT MAX(AuditId) FROM aud.AUD_CAMBIOS), 0);

    BEGIN TRY
        BEGIN TRAN;

        INSERT INTO dbo.FACTURA(NUM_FAC, FEC_FAC, COD_CLI, FEC_CAN, EST_FAC, COD_VEN, POR_IGV)
        VALUES (@NumFac, CONVERT(date, GETDATE()), @CodCli, NULL, '1', @CodVen, 0.18);

        INSERT INTO dbo.DETALLE_FACTURA(NUM_FAC, COD_PRO, CAN_VEN, PRE_VEN)
        VALUES (@NumFac, @CodPro, 2, 10);

        UPDATE dbo.FACTURA
           SET EST_FAC = '2'
         WHERE NUM_FAC = @NumFac;

        UPDATE dbo.DETALLE_FACTURA
           SET CAN_VEN = 3
         WHERE NUM_FAC=@NumFac AND COD_PRO=@CodPro;

        DELETE FROM dbo.DETALLE_FACTURA WHERE NUM_FAC=@NumFac AND COD_PRO=@CodPro;
        DELETE FROM dbo.FACTURA WHERE NUM_FAC=@NumFac;

        ROLLBACK;

        SELECT TOP 200 AuditId, Tabla, Operacion, BusinessKey, ChangeDtm
        FROM aud.AUD_CAMBIOS
        WHERE AuditId > @Antes
          AND (BusinessKey LIKE CONCAT('NUM_FAC=', @NumFac, '%')
               OR BusinessKey LIKE CONCAT('%NUM_FAC=', @NumFac, '%'))
        ORDER BY AuditId;

    END TRY
    BEGIN CATCH
        IF @@TRANCOUNT > 0 ROLLBACK;
        THROW;
    END CATCH
END
GO

